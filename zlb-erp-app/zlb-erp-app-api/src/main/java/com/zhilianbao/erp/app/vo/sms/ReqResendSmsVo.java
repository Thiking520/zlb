package com.zhilianbao.erp.app.vo.sms;

import com.zhilianbao.erp.app.vo.ReqBaseVo;

import java.io.Serializable;

public class ReqResendSmsVo extends ReqBaseVo implements Serializable{

    private static final long serialVersionUID = 4189371504526086566L;

    private String smsCode;//短信编码
    private String DriverNo;//显示运单号
    private Boolean isAll;//是否是全部重发 1 是 0 否

    public static long getSerialVersionUID() {
        return serialVersionUID;
    }

    public String getSmsCode() {
        return smsCode;
    }

    public void setSmsCode(String smsCode) {
        this.smsCode = smsCode;
    }

    public String getDriverNo() {
        return DriverNo;
    }

    public void setDriverNo(String driverNo) {
        DriverNo = driverNo;
    }

    public Boolean getIsAll() {
        return isAll;
    }

    public void setIsAll(Boolean all) {
        isAll = all;
    }
}
