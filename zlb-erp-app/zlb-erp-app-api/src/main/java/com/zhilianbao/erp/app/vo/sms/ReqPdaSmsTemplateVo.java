package com.zhilianbao.erp.app.vo.sms;

import com.zhilianbao.erp.app.vo.ReqBaseVo;

import java.io.Serializable;
import java.util.List;


public class ReqPdaSmsTemplateVo extends ReqBaseVo implements Serializable{

    private static final long serialVersionUID = 130674735852597337L;


    private List<String> driverNos;//接收短信运单列表
    //以上为扩充属性

    /**
     * 模板编码
     */
    private String templateCode;

    /**
     * 模板主题
     */
    private String templateTheme;

    /**
     * 模版短信内容
     */
    private String templateContent;

    public static long getSerialVersionUID() {
        return serialVersionUID;
    }

    public List<String> getDriverNos() {
        return driverNos;
    }

    public void setDriverNos(List<String> driverNos) {
        this.driverNos = driverNos;
    }

    public String getTemplateCode() {
        return templateCode;
    }

    public void setTemplateCode(String templateCode) {
        this.templateCode = templateCode;
    }

    public String getTemplateTheme() {
        return templateTheme;
    }

    public void setTemplateTheme(String templateTheme) {
        this.templateTheme = templateTheme;
    }

    public String getTemplateContent() {
        return templateContent;
    }

    public void setTemplateContent(String templateContent) {
        this.templateContent = templateContent;
    }
}