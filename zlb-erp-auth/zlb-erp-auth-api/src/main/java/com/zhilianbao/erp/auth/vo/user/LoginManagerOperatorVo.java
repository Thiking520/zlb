package com.zhilianbao.erp.auth.vo.user;

import java.io.Serializable;

public class LoginManagerOperatorVo implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private Long operatorId;
	private String companyName;
	private String companyFullName;
	private String companyExtName;
	private String operatorIdToken;
	private Long companyKey;
	
	public Long getOperatorId() {
		return operatorId;
	}
	public void setOperatorId(Long operatorId) {
		this.operatorId = operatorId;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getCompanyFullName() {
		return companyFullName;
	}
	public void setCompanyFullName(String companyFullName) {
		this.companyFullName = companyFullName;
	}
	public String getCompanyExtName() {
		return companyExtName;
	}
	public void setCompanyExtName(String companyExtName) {
		this.companyExtName = companyExtName;
	}
	public String getOperatorIdToken() {
		return operatorIdToken;
	}
	public void setOperatorIdToken(String operatorIdToken) {
		this.operatorIdToken = operatorIdToken;
	}
	public Long getCompanyKey() {
		return companyKey;
	}
	public void setCompanyKey(Long companyKey) {
		this.companyKey = companyKey;
	}
	
	

}
