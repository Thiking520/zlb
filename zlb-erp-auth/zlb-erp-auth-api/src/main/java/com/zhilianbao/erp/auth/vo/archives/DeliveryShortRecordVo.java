package com.zhilianbao.erp.auth.vo.archives;

import java.io.Serializable;

import com.zhilianbao.erp.common.vo.BaseVo;

public class DeliveryShortRecordVo extends BaseVo implements Serializable {


	/**
	 * 
	 */
	private static final long serialVersionUID = 7172125621047130753L;

	/**
	 * 编号 运营商 + 编号）唯一， 编码规则：001~999 自增
	 */
	private String code;

	/**
	 * 名称
	 */
	private String name;
	
	private String deliveryType;
	
	public String getDeliveryType() {
		return deliveryType;
	}

	public void setDeliveryType(String deliveryType) {
		this.deliveryType = deliveryType;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
